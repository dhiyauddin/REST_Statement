package com.example.demo;

public class Login {
	
	private String username;
	private String password;
	private String company_name;
	private String company_ssm_no;
	private String company_address;
	private String company_contact_no;
	
	public Login () {}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCompany_name() {
		return company_name;
	}

	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}

	public String getCompany_ssm_no() {
		return company_ssm_no;
	}

	public void setCompany_ssm_no(String company_ssm_no) {
		this.company_ssm_no = company_ssm_no;
	}

	public String getCompany_address() {
		return company_address;
	}

	public void setCompany_address(String company_address) {
		this.company_address = company_address;
	}

	public String getCompany_contact_no() {
		return company_contact_no;
	}

	public void setCompany_contact_no(String company_contact_no) {
		this.company_contact_no = company_contact_no;
	}
	
	

}
